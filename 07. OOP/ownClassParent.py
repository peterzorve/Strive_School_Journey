import math

class MathOperations:
    def __init__(self, firstNumber, secondNumber):
        self.firstNumber = firstNumber
        self.secondNumber = secondNumber

    def summation(self):
        return self.firstNumber + self.secondNumber

    def difference(self):
        return self.firstNumber - self.secondNumber    

    def multiplication(self):
        return self.firstNumber * self.secondNumber

    def division(self):
        return self.firstNumber / self.secondNumber
